USE AlterGheist;

CREATE TABLE Moods(
	moodId INT AUTO_INCREMENT PRIMARY KEY,
    moodDate DATE NOT NULL UNIQUE,
    Mood1 NVARCHAR(30) NOT NULL,
    Mood2 NVARCHAR(30) NOT NULL,
    Mood3 NVARCHAR(30) NOT NULL,
    Mood4 NVARCHAR(30) NOT NULL,
    Mood5 NVARCHAR(30) NOT NULL,
    Headlines TEXT NOT NULL
);

CREATE TABLE Bookmarks(
	UserID INT NOT NULL,
    MoodsID INT NOT NULL,
	PRIMARY KEY(UserID, MoodsID),

    FOREIGN KEY (UserID) REFERENCES Users(userID) ON DELETE CASCADE,
	FOREIGN KEY (MoodsID) REFERENCES Moods(moodID) ON DELETE CASCADE
);

CREATE TABLE Users(
	userId INT AUTO_INCREMENT PRIMARY KEY,
    Username NVARCHAR(100) NOT NULL UNIQUE,
    Password CHAR(60) NOT NULL
)

SELECT * FROM Moods;
SELECT * FROM Bookmarks;
SELECT * FROM Users;
